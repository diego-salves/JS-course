// Estruturas de repetição

//for
for (var i = 0; i <= 10; i++) { //executa antes do incremento
    console.log(i);
}

//while
var numero = 0
//while verifica primeiro, por isso declaramos a variável antes
while(numero < 10){
    console.log(numero);
    numero++;
}

//primeiro checa, depois executa

//do-while
var cont=0;
do{
    console.log("Imprime o valor de cont: " +cont);
    cont++;
}while(cont <=5);

//primeiro faz (do), depois checa
